                <h2 class="text-center mb-4">¿Quién Puede Donar Sangre?</h2>
                <p>La donación de sangre es un acto de generosidad que salva vidas. Sin embargo, no todos pueden donar sangre. Aquí te presentamos quiénes son elegibles para donar:</p>
                <h3>Requisitos Básicos para Donar Sangre</h3>
                <p>Para donar sangre, generalmente se deben cumplir los siguientes requisitos básicos:</p>
                <ul>
                    <li>Ser mayor de 18 años (en algunos países puede ser mayor de 16 o 17 años con el consentimiento de los padres o tutores).</li>
                    <li>Pesar al menos 50 kilogramos.</li>
                    <li>Estar en buen estado de salud general.</li>
                    <li>No haber donado sangre en los últimos 56 días (este período puede variar según las regulaciones locales).</li>
                    <li>No tener enfermedades transmisibles por la sangre, como VIH o hepatitis.</li>
                </ul>
                <h3>Restricciones Temporales para Donar Sangre</h3>
                <p>Además de los requisitos básicos, puede haber algunas restricciones temporales que impiden la donación de sangre. Estas incluyen:</p>
                <ul>
                    <li>Recibir una vacuna recientemente (el tiempo de espera puede variar según la vacuna).</li>
                    <li>Estar embarazada o haber dado a luz recientemente (generalmente se requiere esperar al menos 6 meses después del parto).</li>
                    <li>Haber viajado a ciertas áreas donde hay enfermedades transmitidas por mosquitos, como la malaria (el tiempo de espera puede variar).</li>
                    <li>Haber tenido una cirugía recientemente (generalmente se requiere esperar un cierto período de tiempo después de la cirugía).</li>
                </ul>
                <h3>Exclusiones Permanentes para Donar Sangre</h3>
                <p>Algunas condiciones médicas o comportamientos pueden llevar a una exclusión permanente de la donación de sangre. Estas incluyen:</p>
                <ul>
                    <li>Tener hepatitis después de los 11 años de edad.</li>
                    <li>Haber tenido cáncer en el pasado.</li>
                    <li>Utilizar drogas intravenosas no recetadas.</li>
                    <li>Tener ciertas enfermedades cardíacas o pulmonares.</li>
                    <li>Haber estado expuesto al virus del Zika.</li>
                    <li>Tener antecedentes de transfusiones de sangre no seguras.</li>
                </ul>
                <p>Es importante recordar que las regulaciones pueden variar según el país y la institución que lleva a cabo la donación de sangre. Siempre se realizará una evaluación médica antes de aceptar una donación.</p>
