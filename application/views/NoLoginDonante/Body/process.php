                <h2 class="text-center mb-4">Proceso de Donación de Sangre</h2>
                <p>La donación de sangre es un proceso sencillo que salva vidas. Aquí tienes una guía paso a paso sobre qué esperar durante tu donación:</p>
                <h3>Paso 1: Registro</h3>
                <p>Al llegar al centro de donación, te pedirán que completes un formulario con tu información personal, historial médico y consentimiento para donar.</p>
                <h3>Paso 2: Entrevista de Selección</h3>
                <p>Un profesional de la salud revisará tu formulario y te realizará una serie de preguntas sobre tu salud, viajes recientes, historial de enfermedades y otros factores que puedan afectar la elegibilidad para donar sangre.</p>
                <h3>Paso 3: Examen Físico</h3>
                <p>Después de la entrevista, se realizará un breve examen físico para medir tu presión arterial, pulso y niveles de hemoglobina para garantizar que estés en condiciones de donar de manera segura.</p>
                <h3>Paso 4: Donación de Sangre</h3>
                <p>Una vez que hayas pasado la entrevista y el examen físico, te llevarán a la sala de donación donde se realizará la extracción de sangre. Este proceso suele durar entre 5 y 10 minutos.</p>
                <h3>Paso 5: Recuperación</h3>
                <p>Después de la donación, te ofrecerán bebidas y aperitivos para ayudarte a recuperarte. Es importante descansar durante unos minutos antes de volver a tus actividades diarias.</p>
                <h3>Paso 6: Monitoreo y Seguimiento</h3>
                <p>El personal del centro de donación te proporcionará instrucciones sobre cómo cuidar tu brazo después de la donación y te informará sobre posibles efectos secundarios que puedas experimentar. También te indicarán cuándo puedes donar nuevamente.</p>
                <h3>¡Gracias por Donar!</h3>
                <p>Con tu donación de sangre, estás ayudando a salvar vidas y hacer una diferencia significativa en la comunidad. ¡Gracias por tu generosidad y dedicación!</p>
