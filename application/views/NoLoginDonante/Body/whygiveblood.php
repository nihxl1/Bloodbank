            <h2 class="text-center mb-4">¿Por qué donar sangre?</h2>
            <p class="lead">La donación de sangre es un acto altruista que salva vidas. Cada donación puede marcar la diferencia en la vida de personas que necesitan transfusiones de sangre para sobrevivir.</p>
            <h3>¿Por qué necesitamos donantes de sangre en España?</h3>
            <p>En España, la necesidad de donaciones de sangre es constante para satisfacer las demandas de los hospitales y centros médicos. Aquí hay algunos datos importantes:</p>
            <ul>
                <li>Necesitamos al menos 900 donaciones de sangre al día para cubrir las necesidades de los hospitales en toda España.</li>
                <li>Más del 70% de la población necesitará transfusiones de sangre en algún momento de sus vidas.</li>
                <li>La donación de sangre es especialmente crucial para pacientes con cáncer, personas sometidas a cirugías, accidentes graves y mujeres con complicaciones durante el parto.</li>
            </ul>
            <h3>Beneficios de donar sangre:</h3>
            <ul>
                <li>Reduce el riesgo de enfermedades cardíacas.</li>
                <li>Quema calorías.</li>
                <li>Estimula la producción de células sanguíneas.</li>
                <li>Proporciona una sensación de bienestar y satisfacción al ayudar a otros.</li>
            </ul>
            <h3>Datos impactantes:</h3>
            <p>En España, más de 1,5 millones de personas donan sangre cada año. ¡Pero necesitamos más!</p>
            <p>Una sola donación de sangre puede salvar hasta tres vidas.</p>
            <h3>Requisitos para donar sangre:</h3>
            <p>Para ser elegible para donar sangre en España, debes cumplir con los siguientes requisitos:</p>
            <ul>
                <li>Tener entre 18 y 65 años.</li>
                <li>Pesar más de 50 kg.</li>
                <li>Goza de buena salud general.</li>
                <li>No haber donado sangre en los últimos 2 meses.</li>
                <li>No haberse sometido a una cirugía importante en los últimos 6 meses.</li>
            </ul>
            <h3>Proceso de donación de sangre:</h3>
            <p>El proceso de donación de sangre es simple y seguro. Se lleva a cabo en un centro de donación de sangre y generalmente incluye los siguientes pasos:</p>
            <ol>
                <li>Registro y evaluación de elegibilidad.</li>
                <li>Entrevista confidencial sobre antecedentes médicos y viajes.</li>
                <li>Examen físico breve para medir la presión arterial, el pulso y la hemoglobina.</li>
                <li>La donación real de sangre, que suele durar unos 10-15 minutos.</li>
                <li>Refrigerio y descanso después de la donación.</li>
            </ol>
            
