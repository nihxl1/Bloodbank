                <h2 class="text-center mb-4">Requisitos de Salud y Elegibilidad para la Donación de Sangre</h2>
                <p>Donar sangre es una forma sencilla e impactante de contribuir a la salud de tu comunidad. Sin embargo, ciertas condiciones de salud y factores de estilo de vida pueden afectar tu elegibilidad. A continuación, se presentan pautas más detalladas:</p>
                <h3>Requisitos Básicos de Salud</h3>
                <p>Por lo general, los donantes deben cumplir con los siguientes criterios básicos de salud:</p>
                <ul>
                    <li>Estar en buen estado de salud general, sin fiebre ni enfermedades el día de la donación.</li>
                    <li>Tener un nivel de hemoglobina dentro de un rango especificado (generalmente entre 12,5 y 17,5 gramos por decilitro).</li>
                    <li>Pesar al menos 50 kilogramos.</li>
                    <li>No haber donado sangre dentro de un período de tiempo específico (comúnmente 56 días).</li>
                    <li>No estar embarazada o haber dado a luz en los últimos 6-12 meses.</li>
                    <li>No haber recibido una transfusión de sangre dentro de un cierto período.</li>
                </ul>
                <h3>Condiciones Médicas y Medicamentos</h3>
                <p>Algunas condiciones médicas y medicamentos pueden afectar tu elegibilidad para donar sangre. Estas incluyen:</p>
                <ul>
                    <li>Antecedentes de enfermedad cardíaca, accidente cerebrovascular o cirugía cardíaca.</li>
                    <li>Antecedentes de cáncer (excluyendo el cáncer de piel de células basales o escamosas).</li>
                    <li>Uso de ciertos medicamentos, como anticoagulantes o inmunosupresores.</li>
                    <li>Diabetes que requiere insulina u otros medicamentos.</li>
                    <li>Antecedentes de reacciones alérgicas graves.</li>
                </ul>
                <h3>Factores de Estilo de Vida</h3>
                <p>Varios factores de estilo de vida pueden afectar tu elegibilidad para donar sangre:</p>
                <ul>
                    <li>Reciente viaje a áreas con enfermedades endémicas, como malaria o virus Zika.</li>
                    <li>Tatuajes o piercings recientes (generalmente requiere un período de espera de 3 a 12 meses).</li>
                    <li>Historial de uso de drogas intravenosas.</li>
                    <li>Participación en comportamientos sexuales de alto riesgo.</li>
                    <li>Participación en el servicio militar en ciertas regiones.</li>
                </ul>
                <h3>Exclusiones y Diferimientos</h3>
                <p>Algunas condiciones o circunstancias pueden resultar en un diferimiento temporal o una exclusión permanente de la donación de sangre:</p>
                <ul>
                    <li>Cirugía reciente o procedimiento médico importante (requiere un período de espera).</li>
                    <li>Trabajo dental reciente (puede requerir un período de espera).</li>
                    <li>Embarazo, parto o lactancia materna (diferimiento temporal).</li>
                    <li>Antecedentes de ciertas infecciones, como VIH o hepatitis (exclusión permanente).</li>
                </ul>
                <p>Es fundamental proporcionar información precisa sobre tu salud y estilo de vida durante el proceso de selección. Los centros de donación de sangre priorizan la seguridad tanto de los donantes como de los receptores.</p>
                <h3>¿Listo para Donar?</h3>
                <p>Si cumples con los requisitos de salud y criterios de elegibilidad, considera donar sangre hoy mismo para ayudar a salvar vidas en tu comunidad. ¡Tu contribución puede marcar una diferencia significativa!</p>
